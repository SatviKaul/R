 #OPERATORS
 x<-c(1.5,3.9)
 y<-c(9.2,4)
 print(x+y)
 print(x-y)
 print(x*y)
 print(y/x)
 print(x%%y)
 print(x**y)
 print(x**2)
 print(x**3)
 print(1*69)
 print(23*3)

 #RELATIONAL
 x=10.4
 y=8.6
 print(x&y)
 print(x&&y)
 print(x||y)
 print(x==y)
 print(x!=y)

